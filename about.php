<h1 style="font-size: 3.25em; font-weight: bold; text-align:left;color:black">About</h1>
<div class="shadow-hover" style="background-color:white; color:black; padding:32px">
    <p>The UBC Trading Group is a student group established in March 2011 to bring together retail investors and traders within the UBC community and beyond. It was co-founded by Bryan Nguyen and Michael Kim, Sauder School of Business students, with the mission of being able to inspire and self-motivate students to learn to look at the markets daily, read the news regularly, and potentially gain the confidence to invest their own money. Our mission statement is as follows:</p>   
    <p>“To supplement and facilitate the learning process of trading or investing in the capital markets.”</p>
    <p>With a UBCTG membership, through the events and services we hold, we have the #1 priority of providing members with a community of students and professionals to aid and support investment education. From student directed seminars to UBC Trading Floors, this is our first step to providing executive to student level service.</p>
    <p> The current acting President is Phillip Wade and the current acting Vice President is Rachid Mounla.</p>
